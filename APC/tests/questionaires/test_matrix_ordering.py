from codes.questionnaires.matrix_ordering import order_by_col


def test_order_by_col():
    #### CASE 1

    matrix = [[0, 9, 1], [1, 8, 2], [2, 7, 3]]

    assert order_by_col(matrix, 1) == [
        [2, 7, 3],
        [1, 8, 2],
        [0, 9, 1],
    ]

    #### CASE 2

    matrix = [
        [0, 2, 1],
        [0, 1, 1],
        [0, 0, 1],
    ]

    assert order_by_col(matrix, 0) == [
        [0, 0, 1],
        [0, 1, 1],
        [0, 2, 1],
    ]

    #### CASE 3

    matrix = [
        [5, 13, 2],
        [8, 6, 4],
        [10, 10, 9],
        [10, 1, 1],
        [10, 8, 2],
        [6, 0, 2],
        [3, 5, 10],
        [6, 2, 9],
        [12, 4, 2],
        [1, 4, 11],
        [5, 1, 13],
        [12, 11, 9],
        [4, 4, 6],
    ]

    assert order_by_col(matrix, 1) == [
        [6, 0, 2],
        [10, 1, 1],
        [5, 1, 13],
        [6, 2, 9],
        [4, 4, 6],
        [1, 4, 11],
        [12, 4, 2],
        [3, 5, 10],
        [8, 6, 4],
        [10, 8, 2],
        [10, 10, 9],
        [12, 11, 9],
        [5, 13, 2],
    ]
